fun main(args: Array<String>) {
    val lines = input02b2.lines()
    var lastGoodLineIndex = 0
    var lastGoodLineIndex2 = 0
    var lastBadCharIndex = 0
    for (lineIndex in lines.indices) {
        if (lineIndex+1 == lines.count()) break

        val actualLine = lines[lineIndex].toCharArray()
        //println("Aktuelle Zeile: "+ actualLine.toList())
        for (nextLineIndex in lineIndex+1 until lines.count()-1) {

            val nextLine = lines[nextLineIndex].toCharArray()
            //println("Vergleichs Zeile: " + nextLine.toList())
            var countErrors = 0
            for (charIndex in actualLine.indices) {
                //println( " Char: " + actualLine[charIndex] + " NextLineChar: " + nextLine[charIndex])
                if (actualLine[charIndex] == nextLine[charIndex]) //println("Gefunden: " + actualLine[charIndex])
                else {
                    countErrors += 1
                    lastBadCharIndex = charIndex
                }
                if (countErrors > 1) {
                    break
                } else {
                    lastGoodLineIndex = lineIndex
                    lastGoodLineIndex2 = nextLineIndex
                }
            }

            if (countErrors <= 1) {
                println("Fehler: " + countErrors)
                println("Zeile 1: " + lines[lastGoodLineIndex])
                println("Zeile 2: " + lines[lastGoodLineIndex2])
                println("Gefunden: " + actualLine[lastBadCharIndex])
            }
        }

    }
}